window.onload = onPageLoad;

function onPageLoad() {
  renderGame();
  setInterval(renderGame, 1000);
}

NSG.MILLIS_PER_DAY    = 86400000;
NSG.MILLIS_PER_HOUR   = 3600000;
NSG.MILLIS_PER_MINUTE = 60000;
NSG.MILLIS_PER_SECOND = 1000;

function formatTimeDifference(timeDiffMillis) {
  var days = Math.floor(timeDiffMillis / NSG.MILLIS_PER_DAY);
  var hours = Math.floor((timeDiffMillis - (days * NSG.MILLIS_PER_DAY)) / NSG.MILLIS_PER_HOUR);
  var minutes = Math.floor((timeDiffMillis - ((hours * NSG.MILLIS_PER_HOUR) + (days * NSG.MILLIS_PER_DAY))) / NSG.MILLIS_PER_MINUTE);
  var seconds = Math.floor((timeDiffMillis - ((hours * NSG.MILLIS_PER_HOUR) + (days * NSG.MILLIS_PER_DAY) + (minutes * NSG.MILLIS_PER_MINUTE))) / NSG.MILLIS_PER_SECOND);

  return [
    days + ' days',
    hours + ' hours',
    minutes + ' minutes',
    seconds + ' seconds'
  ].join('<br>');
}

function getGame(pred) {
  var game;

  for (var i = 0; i < NSG.GAME_DATA.length; i++) {
    game = NSG.GAME_DATA[i];

    if (pred(game)) {
      return game
    }
  }
}

function futureGamePred() {
  var now = new Date().getTime();

  return function(game) {
    return game.time_in_milliseconds > now;
  }
}

function isHomeGame(game) {
  return game.location === 'SEATTLE, WA';
}

function futureHomeGamePred() {
  var isFutureGame = futureGamePred();

  return function(game) {
    return isFutureGame(game) && isHomeGame(game);
  }
}

function renderGame() {
  var countdown = document.getElementsByClassName('countdown')[0];
  var nowMillis = new Date().getTime();
  var nextGame = getGame(futureGamePred());
  var timeDiffMillis = nextGame.time_in_milliseconds - nowMillis;
  var homeAway = nextGame.location === "SEATTLE, WA" ? 'Home' : 'Away';

  countdown.innerHTML = formatTimeDifference(timeDiffMillis);

  document.getElementsByClassName('game-content')[0].innerHTML = nextGame.date + '<br>' + [homeAway, 'vs', nextGame.matchup].join(' ');

  if (homeAway === 'Away') {
    displayNextHomeGame(nowMillis);
  }
}

function displayNextHomeGame(now) {
  var nextHomeGame = getGame(futureHomeGamePred());

  document.getElementsByClassName('home-game-header')[0].textContent = 'Next home game';
  document.getElementsByClassName('home-game-content')[0].innerHTML = nextHomeGame.date + '<br>' + 'vs ' + nextHomeGame.matchup;
}
